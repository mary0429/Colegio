    private void llenarTabla(Colegio colegio){
        DefaultTableModel modelo = (DefaultTableModel)tabla1.getModel();
        
        Vector fila = new Vector();
        
        fila.add(colegio.getNombre());
        fila.add(colegio.getDueno());
        fila.add(colegio.getNumeroDeProfesores());
        fila.add(colegio.getPrivado());
        fila.add(colegio.getCalendarioB());
        
        modelo.addRow(fila);
        
        JOptionPane.showMessageDialog(this, "Informacion de la institucion educativa exitosamente registrada.");
    }
