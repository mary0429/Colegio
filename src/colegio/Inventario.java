    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        
        Colegio colegio = new Colegio("", "", true, true, new date(), 0, 0, 0, 0);
        
        colegio.setNombre(campoNombre.getText());
        campoNombre.setText("");
        
        colegio.setDueno(campoFundador.getText());
        campoFundador.setText("");
        
        colegio.setNumeroDeProfesores((Integer)campoProfesores.getValue());
        campoProfesores.setValue(0);
        
        colegio.setPrivado(opcionSi1.isSelected());
        
        colegio.setCalendarioB(opcionSi2.isSelected());
        
        llenarTabla(colegio);
    }//GEN-LAST:event_botonIngresarActionPerformed

